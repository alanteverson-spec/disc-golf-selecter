const discs = Object.entries(manufacturerCatalog).flatMap(([manufacturer, molds]) =>
  molds.map(([name, speed, glide, turn, fade]) => ({
    name,
    manufacturer,
    speed,
    glide,
    turn,
    fade
  }))
);

const input = document.querySelector("#disc-input");
const results = document.querySelector("#results");
const topDiscSelect = document.querySelector("#top-disc-select");
const toggleName = document.querySelector("#toggle-name");
const togglePopular = document.querySelector("#toggle-popular");
const totalManufacturers = Object.keys(manufacturerCatalog).length;
const topSellingMolds = [
  "Destroyer",
  "Buzzz",
  "Zone",
  "Wraith",
  "Roc3",
  "Teebird",
  "Aviar",
  "Firebird",
  "Leopard3",
  "Luna"
];

toggleName.addEventListener("click", () => {
  setSearchMode("name");
});

togglePopular.addEventListener("click", () => {
  setSearchMode("popular");
});

input.addEventListener("keydown", (event) => {
  if (event.key !== "Enter") return;
  runSearch(input.value.trim());
});

topDiscSelect.addEventListener("change", () => {
  const selectedDisc = topDiscSelect.value;
  if (!selectedDisc) return;
  input.value = selectedDisc;
  runSearch(selectedDisc);
});

function setSearchMode(mode) {
  const useNameSearch = mode === "name";

  toggleName.classList.toggle("is-active", useNameSearch);
  togglePopular.classList.toggle("is-active", !useNameSearch);
  toggleName.setAttribute("aria-selected", useNameSearch ? "true" : "false");
  togglePopular.setAttribute("aria-selected", useNameSearch ? "false" : "true");

  input.classList.toggle("is-hidden", !useNameSearch);
  topDiscSelect.classList.toggle("is-hidden", useNameSearch);

  if (useNameSearch) {
    input.focus();
  } else {
    topDiscSelect.focus();
  }
}

function runSearch(query) {
  if (!query) {
    renderEmpty(
      `Type a disc name and press Enter to compare flight numbers across ${totalManufacturers} major manufacturers.`
    );
    return;
  }

  const sourceDisc = findSourceDisc(query);
  if (!sourceDisc) {
    renderEmpty(`No disc found for "${query}" in the current catalog. Try another mold name.`);
    return;
  }

  const matches = findComparableDiscs(sourceDisc);
  renderMatches(sourceDisc, matches);
}

function findSourceDisc(query) {
  const normalized = query.toLowerCase();

  const exact = discs.find((disc) => disc.name.toLowerCase() === normalized);
  if (exact) return exact;

  return discs.find((disc) => disc.name.toLowerCase().includes(normalized));
}

function flightDistance(a, b) {
  const speedGap = Math.abs(a.speed - b.speed);
  const glideGap = Math.abs(a.glide - b.glide);
  const turnGap = Math.abs(a.turn - b.turn);
  const fadeGap = Math.abs(a.fade - b.fade);

  return speedGap * 1.15 + glideGap + turnGap + fadeGap * 1.1;
}

function findComparableDiscs(sourceDisc) {
  return discs
    .filter(
      (disc) =>
        !(disc.name === sourceDisc.name && disc.manufacturer === sourceDisc.manufacturer) &&
        disc.manufacturer !== sourceDisc.manufacturer
    )
    .map((disc) => ({
      ...disc,
      score: flightDistance(sourceDisc, disc)
    }))
    .filter((disc) => disc.score <= 3.1)
    .sort((a, b) => a.score - b.score)
    .slice(0, 12);
}

function renderMatches(sourceDisc, matches) {
  results.classList.remove("empty");

  if (!matches.length) {
    results.innerHTML = `
      <h2>No close matches found</h2>
      <p class="source">
        ${sourceDisc.name} (${sourceDisc.manufacturer}) — ${flightLabel(sourceDisc)}
      </p>
      <p>Try a different disc name for broader comparisons.</p>
    `;
    return;
  }

  const items = matches
    .map(
      (disc) => `
      <li class="match-item">
        <div class="match-main">
          <div class="disc-name">${disc.name}</div>
          <div class="manufacturer">${disc.manufacturer}</div>
        </div>
        <div class="badges">
          <span class="badge">${flightLabel(disc)}</span>
          <span class="badge match">Similarity ${Math.max(0, 100 - disc.score * 15).toFixed(0)}%</span>
        </div>
      </li>
    `
    )
    .join("");

  results.innerHTML = `
    <h2>Comparable discs for ${sourceDisc.name}</h2>
    <p class="source">${sourceDisc.manufacturer} — ${flightLabel(sourceDisc)}</p>
    <p class="source">Showing ${matches.length} matches from ${totalManufacturers} major manufacturers (${discs.length} molds in catalog).</p>
    <ul class="match-list">${items}</ul>
  `;
}

function renderEmpty(message) {
  results.classList.add("empty");
  results.innerHTML = `<p>${message}</p>`;
}

function flightLabel(disc) {
  return `${disc.speed} / ${disc.glide} / ${disc.turn} / ${disc.fade}`;
}

function populateTopDiscSelect() {
  topSellingMolds.forEach((mold) => {
    const option = document.createElement("option");
    option.value = mold;
    option.textContent = mold;
    topDiscSelect.append(option);
  });
}

populateTopDiscSelect();
setSearchMode("name");

renderEmpty(
  `Type a disc name and press Enter to compare flight numbers across ${totalManufacturers} major manufacturers (${discs.length} molds loaded).`
);
